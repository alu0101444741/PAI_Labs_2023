/**
 * @desc Returns a string "One for X, one for me" where X is a given name.
 * By default, X is the string "you"
 * @param {string} name
 * @return {string} One for 'name', one for me
 */
export const twoFer = (name = 'you') => `One for ${name}, one for me.`;
